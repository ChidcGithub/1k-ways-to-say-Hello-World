#include <iostream>

int main() {
    std::cout << (char)((1 << 6) + (1 << 3))                                   // 'H'  = 72
              << (char)((1 << 6) + (1 << 5) + (1 << 2) + 1)                   // 'e'  = 101
              << (char)((1 << 6) + (1 << 5) + (1 << 3) + (1 << 2))             // 'l'  = 108
              << (char)((1 << 6) + (1 << 5) + (1 << 3) + (1 << 2))             // 'l'  = 108
              << (char)((1 << 6) + (1 << 5) + (1 << 3) + (1 << 2) + (1 << 1) + 1) // 'o' = 111
              << (char)((1 << 5) + (1 << 3) + (1 << 2))                       // ','  = 44
              << (char)((1 << 5))                                             // ' '  = 32
              << (char)((1 << 6) + (1 << 4) + (1 << 2) + (1 << 1) + 1)        // 'W'  = 87
              << (char)((1 << 6) + (1 << 5) + (1 << 3) + (1 << 2) + (1 << 1) + 1) // 'o' = 111
              << (char)((1 << 6) + (1 << 5) + (1 << 4) + (1 << 1))            // 'r'  = 114
              << (char)((1 << 6) + (1 << 5) + (1 << 3) + (1 << 2))             // 'l'  = 108
              << (char)((1 << 6) + (1 << 5) + (1 << 2))                       // 'd'  = 100
              << (char)((1 << 5) + 1)                                         // '!'  = 33
              << std::endl;
    return 0;
}
